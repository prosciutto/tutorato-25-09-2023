package it.unisa.studenti.saporito.f;

public class Calcolatrice {
    public int somma(int a, int b) {
        return a + b;
    }
    public int differenza(int a, int b) {
        return a - b;
    }

        public int prodotto(int a, int b) {
        return a * b;
    }
    public int divisione(int a, int b) {
        return a / b;
    }
    public int modulo(int a, int b) {
        return a % b;
    }
    public int potenza(int a, int b) {
        return (int) Math.pow(a, b);
    }
    public int elevato(int a, int b) {
        return (int) Math.pow(a, b);
    }
    public int primo(int a) {
        return (int) Math.pow(a, 2);
    }

}